/* Тема 5: балансировка матрицы требований (адекватность через компромиссы) */
function mechGauge(ses){
  var cfg = GAUGE[player().izdelie] || GAUGE.tvs;
  var pv = cfg.params.map(function(){ return 50; });
  function dval(d){ var v=50; for(var j=0;j<pv.length;j++) v+=d.w[j]*(pv[j]-50); return Math.max(0,Math.min(100,Math.round(v))); }
  function inband(d,v){ return v>=d.lo && v<=d.hi; }
  function close(d,v){ if(inband(d,v)) return 1; var dist=v<d.lo?d.lo-v:v-d.hi; return Math.max(0,1-dist/22); }
  function adeq(){ var s=0; cfg.disc.forEach(function(d){ s+=close(d,dval(d)); }); return s/cfg.disc.length; }
  function feedback(){
    var worst=null,wc=2; cfg.disc.forEach(function(d){ var v=dval(d),c=close(d,v); if(c<wc){wc=c;worst={d:d,v:v};} });
    if(wc>=0.999) return "Все требования в целевом диапазоне - матрица сбалансирована, можно фиксировать двойник.";
    var d=worst.d,v=worst.v;
    return "«"+d.k+"» "+(v<d.lo?"ниже":"выше")+" требования (цель "+d.lo+"-"+d.hi+", сейчас "+v+"). Помни про компромисс: подтянешь одно - просядет другое, ищи баланс.";
  }
  function draw(){
    var a=adeq(), pct=Math.round(a*100);
    var inN=cfg.disc.filter(function(d){return inband(d,dval(d));}).length;
    var pass=inN===cfg.disc.length;
    var bars=cfg.disc.map(function(d){ var v=dval(d), ok=inband(d,v);
      return "<div class='disc'><div class='dl'><span>"+esc(d.k)+"</span><span class='"+(ok?"okv":"badv")+"'>"+v+(ok?" ✓":"")+"</span></div>"+
        "<div class='dtrack'><span class='band' style='left:"+d.lo+"%;width:"+(d.hi-d.lo)+"%'></span>"+
        "<i class='"+(ok?"ok":"bad")+"' style='width:"+v+"%'></i></div></div>";
    }).join("");
    var sliders=cfg.params.map(function(nm,i){
      return "<div class='slider'><label><span>"+esc(nm)+"</span><span class='muted'>"+pv[i]+"</span></label>"+
        "<input type='range' min='0' max='100' value='"+pv[i]+"' data-i='"+i+"'></div>";
    }).join("");
    app.innerHTML=stationShell(ses.s,1,
      "<p class='muted'>Сбалансируй <b>матрицу требований</b> двойника («"+esc(cfg.name)+"»): двигай конструктивные параметры так, чтобы ВСЕ дисциплины попали в зеленый целевой диапазон. Показатели противоречивы - в этом суть цифрового двойника: компромисс между целевыми показателями и ресурсными ограничениями.</p>"+
      "<div class='discs'>"+bars+"</div>"+
      "<div class='gauge'><div class='gnum'>"+a.toFixed(2)+"</div>"+
      "<div class='muted' style='margin-top:-2px'>адекватность · "+inN+" из "+cfg.disc.length+" требований в норме</div>"+
      "<div class='gtrack'><i style='width:"+pct+"%'></i></div></div>"+
      "<div class='sliders'>"+sliders+"</div>"+
      "<div class='fb"+(pass?" ok":"")+"'>"+feedback()+"</div>"+
      "<div style='margin-top:12px'><button class='btn' id='chk' "+(pass?"":"disabled")+">"+(pass?"Зафиксировать сбалансированный двойник":"Не все требования в норме")+"</button></div>");
    Array.prototype.forEach.call(document.querySelectorAll("input[type=range]"),function(r){
      r.oninput=function(){ pv[parseInt(r.getAttribute("data-i"),10)]=parseInt(r.value,10); draw(); };
    });
    var chk=document.getElementById("chk");
    if(chk) chk.onclick=function(){ ses.mechScore=Math.round(adeq()*100); runQuiz(ses); };
    bindLogout();
  }
  draw();
}

/* Тема 10: программа виртуальных испытаний - охват целевых показателей против ресурса стенда */
