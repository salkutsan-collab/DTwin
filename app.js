/* ---------- механики ---------- */
function runMechanic(ses){
  var m=ses.s.mech;
  if(m==="sort") return mechSort(ses);
  if(m==="pipeline") return mechPipeline(ses);
  if(m==="trials") return mechTrials(ses);
  if(m==="buckets") return mechBuckets(ses);
  if(m==="include") return mechInclude(ses);
  if(m==="match") return mechMatch(ses);
  if(m==="order") return mechOrder(ses);
  if(m==="drag") return mechDrag(ses);
  if(m==="links") return mechLinks(ses);
  return mechGauge(ses);
}

