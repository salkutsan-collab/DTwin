function pickQuestions(theme,k){
  function diff(q){
    var c=[],w=[]; q.options.forEach(function(o){ (o.c?c:w).push((o.t||"").length); });
    var avg=function(a){ return a.length? a.reduce(function(x,y){return x+y;},0)/a.length : 0; };
    var base=q.options.length>=4?1:0;
    return base - Math.abs(avg(c)-avg(w))/45;
  }
  // 1) отраслевые вопросы изделия в приоритете
  var picked=shuffle(industryQuestions(theme)).slice(0,k);
  // 2) добор из банка курса (сложные сначала, без повторов по тексту вопроса)
  if(picked.length<k){
    var seen={}; picked.forEach(function(q){ seen[q.q]=1; });
    var qs=themeQuestions(theme).filter(function(q){ return !seen[q.q]; });
    qs.sort(function(a,b){ return diff(b)-diff(a); });
    var hard=qs.slice(0, Math.max(k, Math.ceil(qs.length*0.6)));
    picked=picked.concat(shuffle(hard).slice(0, k-picked.length));
  }
  return shuffle(picked).map(prepQ);
}

