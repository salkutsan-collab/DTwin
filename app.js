/* кольцо-цикл финала (SVG): узлы по статусу + дуга готовности */
function finalRing(idx, results){
  var n=FINAL.stages.length, R=104, cx=130, cy=130;
  var done=0, nodes="";
  for(var k=0;k<n;k++){
    var ang=-Math.PI/2 + k*2*Math.PI/n;
    var x=cx+R*Math.cos(ang), y=cy+R*Math.sin(ang);
    var st = (results[k]==null) ? (k===idx?"cur":"pend") : (results[k]?"ok":"bad");
    if(results[k]!=null) done++;
    nodes+="<circle cx='"+x.toFixed(1)+"' cy='"+y.toFixed(1)+"' r='14' class='fnode "+st+"'></circle>"+
      "<text x='"+x.toFixed(1)+"' y='"+(y+4).toFixed(1)+"' text-anchor='middle' class='fnum'>"+(k+1)+"</text>";
  }
  var circ=2*Math.PI*R, prog=done/n;
  return "<svg class='fring' viewBox='0 0 260 260'>"+
    "<circle cx='"+cx+"' cy='"+cy+"' r='"+R+"' class='ftrack'></circle>"+
    "<circle cx='"+cx+"' cy='"+cy+"' r='"+R+"' class='farc' transform='rotate(-90 "+cx+" "+cy+")' "+
      "stroke-dasharray='"+circ.toFixed(1)+"' stroke-dashoffset='"+(circ*(1-prog)).toFixed(1)+"'></circle>"+
    nodes+"</svg>";
}

